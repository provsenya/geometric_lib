def area(a, b, c):
    '''Принимает числа (a, b, c) - длины сторон, возвращает их сумму деленную на 2 - полупериметр треугольника'''
    return (a + b + c) / 2


def perimeter(a, b, c):
    '''Принимает числа (a, b, c) - длины сторон, возвращает их сумму - приметр треугольника'''
    return a + b + c
